public class CatalogoValor extends ModelAuditoria {
	private String codigoValorCatalogo;
	private String codigoCatalogoTipo;
	private String valorCatalogo;
	private String tipoDatoValor;
	private String nombreCatalogoValor;
	private String descripcionCatalogo;
	private String color;
	private String fontIco;
	
	
	public String getCodigoValorCatalogo() {
		return codigoValorCatalogo;
	}
	public void setCodigoValorCatalogo(String codigoValorCatalogo) {
		this.codigoValorCatalogo = codigoValorCatalogo;
	}
	public String getCodigoCatalogoTipo() {
		return codigoCatalogoTipo;
	}
	public void setCodigoCatalogoTipo(String codigoCatalogoTipo) {
		this.codigoCatalogoTipo = codigoCatalogoTipo;
	}
	public String getValorCatalogo() {
		return valorCatalogo;
	}
	public void setValorCatalogo(String valorCatalogo) {
		this.valorCatalogo = valorCatalogo;
	}
	public String getTipoDatoValor() {
		return tipoDatoValor;
	}
	public void setTipoDatoValor(String tipoDatoValor) {
		this.tipoDatoValor = tipoDatoValor;
	}
	public String getNombreCatalogoValor() {
		return nombreCatalogoValor;
	}
	public void setNombreCatalogoValor(String nombreCatalogoValor) {
		this.nombreCatalogoValor = nombreCatalogoValor;
	}
	public String getDescripcionCatalogo() {
		return descripcionCatalogo;
	}
	public void setDescripcionCatalogo(String descripcionCatalogo) {
		this.descripcionCatalogo = descripcionCatalogo;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public String getFontIco() {
		return fontIco;
	}
	public void setFontIco(String fontIco) {
		this.fontIco = fontIco;
	}
	
	
	
	
}
